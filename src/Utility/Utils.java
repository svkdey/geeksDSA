package Utility;
import java.util.Arrays;

public class Utils {
	public static void p1Darray(int arr[]) {
		System.out.println(Arrays.toString(arr));
	}

	public static void p1Darray(long arr[]) {
		System.out.println(Arrays.toString(arr));
	}

	public static void p2Darray(int arr[][]) {
		for (int i = 0; i < arr.length; i++) {
			System.out.println(Arrays.toString(arr[i]));
		}
	}

	public static void p2Darray(long arr[][]) {
		for (int i = 0; i < arr.length; i++) {
			System.out.println(Arrays.toString(arr[i]));
		}
	}

	
}
